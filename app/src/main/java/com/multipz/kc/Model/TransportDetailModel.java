public class TransportDetailModel {
    private String location, pay_amount, revert_amount, created_date;

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getPay_amount() {
        return pay_amount;
    }

    public void setPay_amount(String pay_amount) {
        this.pay_amount = pay_amount;
    }

    public String getRevert_amount() {
        return revert_amount;
    }

    public void setRevert_amount(String revert_amount) {
        this.revert_amount = revert_amount;
    }

    public String getCreated_date() {
        return created_date;
    }

    public void setCreated_date(String created_date) {
        this.created_date = created_date;
    }
}
